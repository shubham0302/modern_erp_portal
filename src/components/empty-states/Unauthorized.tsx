import { Button } from "@/components/base/Button";
import { useToggle } from "@/hooks/useToggle";
import { usePermissionStore } from "@/store/usePermissions";
import { getFirstAccessibleRoute } from "@/utils/getFirstAccessibleRoute";
import { Link, useSearch } from "@tanstack/react-router";
import LogoutDialog from "../shared/LogoutDialog";

interface UnauthorizedProps {
  hideIcon?: boolean;
  hideCta?: boolean;
}

const Unauthorized: React.FC<UnauthorizedProps> = ({ hideIcon, hideCta }) => {
  const { redirectTo } = useSearch({ strict: false });
  const permissions = usePermissionStore((s) => s.permissions);
  const targetRoute = redirectTo || getFirstAccessibleRoute(permissions);
  const logoutDialog = useToggle();
  const hasAnyPermission = Object.values(permissions).some(
    (p) => p?.canRead === true,
  );

  return (
    <div className="flex flex-col items-center gap-y-2">
      {!hideIcon && (
        <div
          className="fall"
          style={{
            width: 100,
            height: 100,
          }}
        >
          <div className="block dark:hidden">
            <Graphic />
          </div>
          <div className="hidden dark:block">
            <DarkGraphic />
          </div>
        </div>
      )}
      <h4 className="text-nl-700 dark:text-nd-100 text-center font-medium">
        Unauthorized
      </h4>
      <p className="text-nl-600 dark:text-nd-300 text-center">
        {hasAnyPermission
          ? "You are not allowed to access this page. Please contact admin to grant permissions."
          : "You don't have any permission to access any page, Please contact admin to grant permissions"}
      </p>
      {!hideCta && (
        <div className="fall mt-4 gap-4">
          {permissions && hasAnyPermission && (
            <Link to={targetRoute}>
              <Button>Go Home</Button>
            </Link>
          )}
          <Button onClick={logoutDialog.open} color="neutral">
            Logout
          </Button>
          <LogoutDialog
            isOpen={logoutDialog.isOpen}
            close={logoutDialog.close}
          />
        </div>
      )}
    </div>
  );
};

